    public String[] passwordGen(String[] array, int index, RANDOMPASS rp, String output) {
        input = jTextField1.getText();
        String array2[] = input.split("\\+");
        for (int i = 1; i < array.length; i++) {
            array[i] = array[i].replaceAll("[^0-9]", "");
            array[i] = array[i] += output;
            array2[i] = rp.RandomPASS(Integer.parseInt(DBSZAM.getText()), NagyCHEK.isSelected(), kisCHEK.isSelected(), specCHEK.isSelected(), szamokCHEK.isSelected());
            array[i] = ++index + "," + array[i] + array2[i];
            

        }
        return array2;
    }

    public String[] CharSplitBy11Count() {
        input  = jTextField1.getText();
        int count = 0;
        int RowNum = 0;
        int totalSubstrings = (int) Math.ceil((double) input.length() / substringSize);
        strSubstrings = new String[totalSubstrings];
        String password [] = new String[totalSubstrings];
        input = input.replaceAll("[^0-9]", "");
        int szam = 0;
        for (int i = 0; i < input.length(); i = i + substringSize) {
            strSubstrings[szam++] = input.substring(i, Math.min(i + substringSize, input.length()));
            strSubstrings[count] = strSubstrings[count].replaceAll("[^0-9]", "");
            strSubstrings[count] = strSubstrings[count].replaceFirst("06", "36");
            strSubstrings[count] = ++RowNum +","+ strSubstrings[count] + output;
            password[count] = rp.RandomPASS(Integer.parseInt(DBSZAM.getText()), NagyCHEK.isSelected(), kisCHEK.isSelected(), specCHEK.isSelected(), szamokCHEK.isSelected());
            strSubstrings[count] =  strSubstrings[count] + password[count];
            jList2.setListData(password);
            count++;
        }
        return strSubstrings;
    }


