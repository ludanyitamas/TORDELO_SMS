    public String tanusitvanyJelszo(){
        String telszam = telszamTEXT.getText();
        String pass = passText.getText();
        int sorok = 1;
        telszam = telszam.replaceAll("[^0-9]", "");
        String firs4chAR=telszam.substring(0, 4);
        if(firs4chAR.equals("+363") || firs4chAR.equals("+362") || firs4chAR.equals("+367")){
            telszam = telszam.replace("+", "");
        }
        else if(firs4chAR.equals("0630") || firs4chAR.equals("0620") || firs4chAR.equals("0670")){
            
            telszam = telszam.replaceFirst("06", "36");
        }
        return sorok + ","+telszam+outputTan+pass;
        
    }
    
    
